   <p>In this Hybrid Mobile App Development with Ionic training course, expert author Chris Griffith will teach you how to use the Ionic framework to create hybrid mobile applications. This course is designed for the absolute beginner, meaning no experience with mobile app development is required.</p>
   <br>
   <p>You will start by learning how to build apps with the Ionic framework. From there, Chris will teach you how to build a to do application, a weather app, and a national park guide app. This video tutorial will then teach you how to debug and test Ionic applications, including working with emulators, testing on iOS and Android devices, and using Ionic view. Finally, you will finish up your Ionic application by preparing it for submission, code signing for iOS and Android, and generating app icons and splash screens.</p>
   <br>
   <p>Once you have completed this computer based training course, you will have learned how to build hybrid mobile applications with the Ionic framework. Working files are included, allowing you to follow along with the author throughout the lessons.</p>
