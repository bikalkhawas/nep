      <strong>SASS</strong>
      <ul>
         <li>Sass (Syntactically Awesome StyleSheets) is an extension of CSS.</li>
         <li>Sass allows you to use things like variables, nested rules, inline imports and more.</li>
         <li>It also helps to keep things organized and allows you to create stylesheets faster.</li>
      </ul>
      <strong>LESS</strong>
      <ul>
         <li>Less (sometimes stylized as LESS) is a dynamic style sheet language that can be compiled into Cascading Style Sheets (CSS) and run on the client side or server side</li>
         <li>Less is influenced by Sass and has influenced the newer "SCSS" syntax of Sass, which adapted its CSS-like block formatting syntax</li>
         <li>Less provides the following mechanisms: variables, nesting, mixins, operators and functions</li>
         <li>Less allows real-time compilation via less.js by the browser</li>
      </ul>
      <strong>PSD to HTML Conversion</strong>
      <ul>
         <li>"PSD to HTML" is a workflow</li>
         <li>First, a web page is designed in a Photoshop Document (PSD) and then converted to code (using HTML, CSS, and JavaScript)</li>
         <li>You could swap Photoshop with any other image editor (like Pixelmator, GIMP, and so on), but the principle is the same</li>
         <li>You should write HTML and CSS that utilizes the imagery you exported from Photoshop</li>
      </ul>
      <strong>Google Material Design Lite</strong>
      <ul>
         <li>Material Design Lite lets you add a Material Design look and feel to your websites</li>
         <li>Pages developed with MDL will adhere to modern web design principles like browser portability, device independence, and graceful degradation</li>
         <li>The MDL component library includes new versions of common user interface controls such as buttons, check boxes, and text fields, adapted to follow Material Design concepts</li>
         <li>The library also includes enhanced and specialized features like cards, column layouts, sliders, spinners, tabs, typography, and more</li>
      </ul>
